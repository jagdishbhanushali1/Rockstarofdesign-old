    $(document).ready(function(){
        $('.country_specific_tabs li').each(function(){
            var country = $(this).attr('country');
            if($('.' + country.toLowerCase() + '_plan').length == 0)
                $(this).remove();
        });
        display_preferred_server_location("us");

        //left position
