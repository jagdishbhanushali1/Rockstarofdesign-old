    function calculate_price(email_accounts, duration, product_key, return_pricing){
        var range_bounds = [], pricing_data = [], break_loop = false, index = 0,current_slab_price_array;
        var discount_range_limit = {'1-5':1, '6-25':3, '26-49':5, '50-200000':10};
        pricing_data['display_better_price'] = false;
        for(var email_account_range in enterpriseemail_pricing[product_key]['email_account_ranges']){
            current_slab_price_array = enterpriseemail_pricing[product_key]['email_account_ranges'][email_account_range]['add'];
            range_bounds = email_account_range.split('-');
            if(break_loop){
                var next_lower_limit = parseInt(range_bounds[0]);
                var in_discount_range = ((next_lower_limit - email_accounts) <= discount_range_limit[pricing_data['email_account_range']]) ? true : false;
                if(in_discount_range && current_slab_price_array[duration] < pricing_data['price']){
                    pricing_data['display_better_price'] = true;
                    pricing_data['accounts_to_purchase'] = parseInt(range_bounds[0]) - email_accounts;
                }
                break;
            }
            else if(email_accounts >= parseInt(range_bounds[0]) && email_accounts <= parseInt(range_bounds[1])){
                if(return_pricing){
                    pricing_data['email_account_range'] = email_account_range;
                    pricing_data['price'] = current_slab_price_array[duration];
                    break_loop = true;
                } else{
                    return current_slab_price_array;
                }
            }
            index += 1;
        }
        return return_pricing ? pricing_data : '';
    }
