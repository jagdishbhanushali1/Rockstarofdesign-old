	$(document).ready(function(){
		$('#field_domains-input').textboxValueToggle('YourWebsite');
    });
	$.fn.textboxValueToggle = function(defaultText) {
		$(this).focus(function() {
				if($(this).val()=== defaultText){
					$(this).val('').removeClass('optionalField');
				}
			}).blur(function() {
				if($(this).val()==='' ){
					$(this).addClass('optionalField').val(defaultText);
				}
			});
	}

    function showsecret() {
        var uk_note = document.getElementById('uk_note');
        var show_secret = document.getElementById('secret');
        var tld_value = document.transferdomain.tlds.value;
        uk_note.style.display = "none";
        if ((tld_value.match(/.*(nz)$/)) || (tld_value == 'de') || (tld_value == 'eu')
            || (tld_value == 'ca') || (tld_value.match(/.*(au)$/))) {
            show_secret.style.display = "";
        }
        else {
            show_secret.style.display = "none";
        }
        if (tld_value.match(/.*(uk)$/)) {
            uk_note.style.display = "";
        }

    }
