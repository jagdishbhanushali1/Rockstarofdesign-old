    <div class="que">Q. What is SSL and what is an SSL Certificate?</div>
    <div class="ans">
        <p>SSL (Secure Sockets Layer) is the standard security technology for setting up an encrypted link between a web server and a browser. This link ensures the privacy of all data passed between the web server and browsers.</p>
        <p>An SSL certificate enables a web server to create an SSL connection. SSL certificates are expected by consumers for any site that handles sensitive information like identity information, credit card numbers, or confidential records.</p>
    </div>

    <div class="que">Q. Why do I need an SSL Certificate?</div>
    <div class="ans">
        <p>If your website requires users to log in, enter personal information such as credit card numbers or view confidential information such as financial statements, you need to keep the data private and assure your Customers that their data is completely safe.</p>
        Use of an SSL digital certificate assures website users of 3 things:
        <ol>
        	<li>1. That the website really is who it claims to be.</li>
            <li>2. That credit card numbers and other confidential information is encrypted (128bit or higher) and cannot be intercepted.</li>
            <li>3. That the data sent and received cannot be tampered with or forged.</li>
        </ol>
    </div>

